def main() -> None:
    with SRC.open(encoding="utf-8") as f:
        data = json.load(f)

    updated = 0
    misses = []
    char_counts = []

    # Reorder keys per supplement so personalized_reasons sits between
    # main_benefits and disclaimer (cosmetic, JSON spec is order-agnostic
    # but humans skim files top-down).
    new_supplements = []
    for s in data["supplements"]:
        sid = s["id"]
        if sid not in UPGRADES:
            misses.append(sid)
            new_supplements.append(s)
            continue
        benefits, reasons = UPGRADES[sid]
        s["main_benefits"] = benefits
        s["personalized_reasons"] = reasons
        for b in benefits:
            char_counts.append(len(b))
        # rebuild dict in desired key order
        ordered = {}
        for k, v in s.items():
            if k == "disclaimer":
                continue
            ordered[k] = v
        ordered["disclaimer"] = s["disclaimer"]
        new_supplements.append(ordered)
        updated += 1

    data["supplements"] = new_supplements

    with SRC.open("w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write("\n")

    if misses:
        print(f"WARN: ids without upgrade entry: {misses}")
    print(f"Updated: {updated}/50 supplements")
    print(
        f"main_benefits sentence length — min={min(char_counts)}, "
        f"max={max(char_counts)}, "
        f"avg={sum(char_counts) / len(char_counts):.1f} chars"
    )

    # Emit personalized_reasons coverage stats per key.
    key_counts = {}
    for sid, (_, reasons) in UPGRADES.items():
        for k in reasons:
            key_counts[k] = key_counts.get(k, 0) + 1
    print("personalized_reasons key coverage:")
    for k, v in sorted(key_counts.items(), key=lambda kv: -kv[1]):
        print(f"  {k}: {v} supplements")


if __name__ == "__main__":
    main()
